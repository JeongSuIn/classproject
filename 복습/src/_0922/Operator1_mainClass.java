package _0922;

public class Operator1_mainClass {
	public static void main(String[] args) {
		int n;
		n = 0;
		System.out.println("n = " +n);
		n = n +1;
		System.out.println("n = " +n);
		n += 1;
		System.out.println("n = " +n);
		
		n++;
		System.out.println("n = " +n);
		++n;
		System.out.println("n = " +n);
		
		n--;
		System.out.println("n = " +n);
		--n;
		System.out.println("n = " +n);
		
	}

}
